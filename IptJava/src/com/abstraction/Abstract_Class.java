package com.abstraction;

public abstract class Abstract_Class {
	public void cycle() {
		System.out.println("Bike is red colour");
	}
	public void bike() {
		System.out.println("Bus colour is yellow");
	}
	public abstract void van();

	public abstract void tembo();

	public abstract void lorry();

	public void bus() {
		System.out.println("Bus colour is blue");
	}

	public abstract void ship();

	public abstract void aeroplane();

}
